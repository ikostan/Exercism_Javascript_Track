export const decodedValue = (colors) => {

  return Number(COLORS.indexOf(colors[0].toLowerCase()).toString() + 
  COLORS.indexOf(colors[1].toLowerCase()).toString());
};

export const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white"];
